export function sectionToPayload(section: FactSection, value: string): CompanyFactPayload {
    return {
        key: section.key,
        title: section.title,
        value,
        category: section.category,
        value_type: 'text',
        priority: section.priority || 'scenario',
        tags: section.tags || [],
        stages: [],
        questions: section.questions || [],
        hint: section.help,
        display_order: 0,
        is_active: true,
    }
}
