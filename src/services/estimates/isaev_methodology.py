"""Isaev Group commercial estimate methodology for LLM extraction."""


ISAEV_ESTIMATE_METHODOLOGY = """
МЕТОДОЛОГИЯ РАСЧЕТА СМЕТЫ ISAEV GROUP

Главный принцип:
- LLM не составляет смету и не выбирает цены.
- LLM извлекает факты из дизайн-проекта.
- Расчет, строки сметы, цены, скидки и Excel делает код.
- Если значение нельзя надежно снять с чертежа, ставь 0 и объясняй причину в notes.
- Но если количество можно посчитать по видимым символам, приборам, легенде,
  подписям или повторяющимся элементам на плане, обязательно считай его.
  Отсутствие итоговой ведомости не является причиной ставить 0.

1. Что включать в коммерческую смету
- Включай только работы, которые относятся к ремонту Исаев Групп.
- Не включай материалы, мебель, технику, окна, входные двери, кондиционеры и натяжные потолки.
- Демонтаж включается только если явно нужен как оплачиваемый объем. Если в проекте есть лист демонтажа, но нет коммерческой детализации, не включай его в факты.
- Работы "уточняется по факту" возвращай количеством 0, но сохраняй заметку в notes.

2. Перепланировка и перегородки
- Площадь комнаты никогда не равна площади перегородок.
- block_partition_area — это площадь новых блочных перегородок: сумма длин новых перегородок × высота.
- Если на листе видна только площадь помещения, не переноси ее в block_partition_area.
- ГКЛ, стеклянные перегородки, закладные и ниши не смешивай с блочными перегородками.
- Новые/заложенные дверные проемы считай отдельно только если они явно указаны.

3. Стены
- wall_area — общая площадь стен под подготовку/шпаклевку/финиш без вычета оконных и дверных проемов, если нет иной ведомости.
- plaster_area — стены под штукатурку по маякам, кроме санузла под плитку.
- bathroom_plaster_area — стены санузла под плитку.
- Если есть развертки стен, используй их как основной источник.
- Если площади стен не подписаны, не угадывай по площади пола. Запиши 0 и note, что требуется развертка/ручной расчет.

4. Электрика
- Разделяй 1 и 2 этап.
- strobe_length — длина штроб, не равна длине кабеля.
- cable_length — длина кабеля.
- strobe_seal_length обычно равна strobe_length, если нет другой информации.
- socket_boxes — количество подрозетников.
- panel_count — только силовой электрощит. Слаботочный щит и Wi-Fi не увеличивают panel_count.
- outlets — установочные розетки 2 этапа.
- switches — выключатели 2 этапа.
- Если на плане видны символы розеток/выключателей, считай их по символам.
- socket_boxes обычно равно количеству силовых розеток и выключателей, если подрозетники явно не разнесены отдельно.
- Если длины трасс не указаны, не ставь площадь × 3 без явного правила проекта; лучше 0 и note.

5. Сантехника
- water_node_count — коллекторный узел/гребенка, обычно 1 при полноценном санузле и кухне.
- hot_water_points и cold_water_points — точки ГВС/ХВС, а не метры труб.
- sewer_points — выводы канализации по приборам.
- water_heater_count — водонагреватель/бойлер.
- ventilation_count — ремонтные выводы вентиляции, если они входят в работу.
- bath_count, installation_count, bath_binding_count — сантехника 2 этапа.
- Если видны приборы, выводи точки по приборам:
  ванна = 2 водяные точки + 1 канализация;
  раковина = 2 водяные точки + 1 канализация;
  унитаз/инсталляция = 1 ХВС + 1 канализация;
  кухонная мойка = 2 водяные точки + 1 канализация;
  посудомойка/стиральная машина = 1 ХВС + 1 канализация.
- Не смешивай стиральную/сушильную машину как отдельные установки, если в смете Исаев их нет.

6. Санузел и плитка
- bathroom_tile_area — плитка санузла: стены + пол, а не только площадь пола.
- Если площадь стен плитки не указана, но есть площадь пола санузла, используй
  осторожную расчетную оценку: площадь стен = периметр санузла × высота облицовки.
  Если периметр не читается, не ставь bathroom_tile_area равным только полу; ставь 0 и note.
- waterproof_area — обмазочная гидроизоляция пола и мокрых зон; если проект дает только площадь пола, не считай ее всей плиткой.
- waterproof_tape_length — длина углов/примыканий; если не видно, 0.
- bathroom_primer_area обычно равна bathroom_tile_area, если грунтуются поверхности под плитку.
- bath_screen_count — экран под ванну, только если ванна и экран явно входят в работы.
- Резы и запилы плитки оставляй 0, если нет точного метража.

7. Чистовые полы
- floor_tile_area — керамогранит пола вне санузла, например вход/кухня/коридор.
- vinyl_area — кварцвинил/ламинат/похожее чистовое покрытие, если это работа Исаев.
- Если на плане напольных покрытий площади подписаны внутри зон, суммируй площади по легенде.
- Не относить всю площадь квартиры в vinyl_area, если часть покрыта плиткой, санузлом или лоджией.
- baseboard_length — периметр плинтуса, не площадь пола.
- Скрытый плинтус и ПУ-плинтус не смешивай без подтверждения.

8. Малярка и обои
- wallpaper_area — площадь стен под обои/поклейку в чистовых работах.
- Если проект показывает отделку стен, но нет площадей, используй развертки. Если развертки не позволяют надежно снять объем, ставь 0.
- Не считай обои по площади пола.

9. Откосы
- rust_panel_length — погонные метры рустпанелей/откосов.
- Количество окон само по себе не равно длине откосов; если нет размеров, ставь 0.

10. Самопроверка перед JSON
- Проверь, что площадь помещения не использована как площадь перегородок.
- Проверь, что плитка санузла не равна только площади пола, если есть стены под плитку.
- Проверь, что panel_count не включает слаботочный щит.
- Проверь, что strobe_length и cable_length не перепутаны.
- Проверь, что натяжные потолки, мебель, окна, двери и кондиционеры не попали в факты.
- Проверь, что все ключи JSON строго из контракта, без новых названий.
""".strip()
